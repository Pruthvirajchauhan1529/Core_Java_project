package Core_java.Control_flow;

class SwitchDemo{
	public static void main(String[] args) {
		int x = 3;

	switch (x){
	case 1:
		System.out.println("Case 1");
		break;
	case 2:
		System.out.println("Case 2");
		break;
	case 3:
		System.out.println("Case 3");
		break;
	default:
		System.out.println("default");
		}
	}
}