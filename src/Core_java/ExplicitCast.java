package Core_java;

class ExplicitCast{
	public static void main(String[] args) {
		int i = 100;
		byte b = (byte)i;

		System.out.println(b);

		int a = 85;
		char ch = (char)a;

		System.out.println(ch);

		int y = 140;
		byte c = (byte)y;

		System.out.println(c);

		int n = 612;
		byte e = (byte)n;
		char h = (char)e;

		System.out.println(h);
	}
}